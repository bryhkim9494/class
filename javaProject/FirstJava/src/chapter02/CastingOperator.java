package chapter02;

public class CastingOperator {
	public static void main(String[] args) {
		char c = 'a'; // a는 유니코드값 97을 가짐
		System.out.println(c);
		
		int n1 = 1;
		
		System.out.println(c+n1);
		
		
		
		int n2 =10;
		System.out.println(n2);
		float n3 =10.0f;
		System.out.println(n2 + n3);
		 
			
		

	}
}
